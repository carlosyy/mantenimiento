    private void guardar(int caso) {
        switch (caso) {
            case 1:
                Contacto familia = new Familia(jtxtNombres1.getText(), jtxtNombres2.getText(), jtxtApellidos1.getText(), jtxtApellidos2.getText(), jtxtTelefono.getText(), jtxtEmail.getText(), jtxtDireccion.getText(), 0, jtxtFechaNac.getText(), jtxtCiudad.getText(), jtxtDocumento.getText(), jtxtEstadoCiv.getText(), jtxtProfesion.getText(), jtxtGenero.getText(), jtxtParentesco.getText(), jtxtApodo.getText());
                contactos.add(familia);
                break;
            case 2:
                Contacto trabajo = new Trabajo(jtxtNombres1.getText(), jtxtNombres2.getText(), jtxtApellidos1.getText(), jtxtApellidos2.getText(), jtxtTelefono.getText(), jtxtEmail.getText(), jtxtDireccion.getText(), 0, jtxtFechaNac.getText(), jtxtCiudad.getText(), jtxtDocumento.getText(), jtxtEstadoCiv.getText(), jtxtProfesion.getText(), jtxtGenero.getText(), jtxtNombreEmpresa.getText(), 0, jtxtCargo.getText());
                contactos.add(trabajo);
                break;
        }
        llenarComboBox();
        JOptionPane.showMessageDialog(null, "La informacion fue guardada exitosamente");
    }

