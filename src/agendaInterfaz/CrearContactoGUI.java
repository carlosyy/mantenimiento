    public void buscar(Contacto contacto) {
        Object[] listaColumna = new Object[4];
        jtxtApellidos1.setText(contacto.getApellido1());
        jtxtApellidos2.setText(contacto.getApellido2());
        jtxtNombres1.setText(contacto.getNombre1());
        jtxtNombres2.setText(contacto.getNombre2());
        jtxtDireccion.setText(contacto.getDireccion());
        jtxtCiudad.setText(contacto.getCiudad());
        jtxtDocumento.setText(contacto.getDocumento());
        jtxtFechaNac.setText(contacto.getFechaNacimiento());
        jtxtEmail.setText(contacto.getEmail());
        jtxtTelefono.setText(contacto.getCelular());
        jtxtGenero.setText(contacto.getGenero());
        jtxtEstadoCiv.setText(contacto.getEstado());
        jtxtProfesion.setText(contacto.getProfesion());
        listaColumna[0] = jtxtNombres1.getText();
        listaColumna[1] = jtxtNombres2.getText();
        listaColumna[2] = jtxtApellidos1.getText();
        listaColumna[3] = jtxtApellidos2.getText();
        modeloT.addRow(listaColumna);
        if (contacto instanceof Familia) {
            jtxtParentesco.setText(((Familia) contacto).getParentesco());
            jtxtApodo.setText(((Familia) contacto).getApodo());
            jtpPanelGral.remove(jpTrabajo);

        } else if (contacto instanceof Trabajo) {
            jtxtNombreEmpresa.setText(((Trabajo) contacto).getNombreEmpresa());
            jtxtCargo.setText(((Trabajo) contacto).getCargo());
            jtpPanelGral.remove(jpFamilia);
        }
    }

    public String Exportar(File archivo, JTable tablaD) {
        String respuesta = "No se realizo con exito la exportaciÃ³n.";
        int numFila = tablaD.getRowCount(), numColumna = tablaD.getColumnCount();
        if (archivo.getName().endsWith("xls")) {
            wb = new HSSFWorkbook();
        } else {
            wb = new XSSFWorkbook();
        }
        Sheet hoja = wb.createSheet("Pruebita");

        try {
            for (int i = -1; i < numFila; i++) {
                Row fila = hoja.createRow(i + 1);
                for (int j = 0; j < numColumna; j++) {
                    Cell celda = fila.createCell(j);
                    if (i == -1) {
                        celda.setCellValue(String.valueOf(tablaD.getColumnName(j)));
                    } else {
                        celda.setCellValue(String.valueOf(tablaD.getValueAt(i, j)));
                    }
                    wb.write(new FileOutputStream(archivo));
                }
            }
            respuesta = "ExportaciÃ³n exitosa.";
        } catch (Exception e) {
        }
        return respuesta;
    }

